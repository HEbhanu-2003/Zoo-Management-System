    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        // TODO add your handling code here:
        String memberID = sid.getText();
        String name = sname.getText();
        String jobroll = sjr.getText();
        String sageText = sage.getText();
        String contactnumber = sconnum.getText();

        
        if(memberID.isEmpty() || name.isEmpty() || jobroll.isEmpty() || sageText.isEmpty() || contactnumber.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Please Fill In All Fields!", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int age;
        try {
          age = Integer.parseInt(sageText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Age Must Be A Valid Number!", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
    }//GEN-LAST:event_jButton3ActionPerformed
        String gender;
        
        if(smale.isSelected())
        {
            gender = "male";
        }
        else
        {
            gender = "female";
        }
    // Success - you can now store or process the data
    zc.addStaff(memberID, name, jobroll, age, contactnumber, gender);
    JOptionPane.showMessageDialog(this, "Staff Member Details Submitted Successfully!");
    }
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String staffID = sid.getText();
        Staff us = zc.searchStaff(staffID);
        if(us != null)
        {
        us.setID(sid.getText());
        us.setName(sname.getText());
        us.setAge(Integer.parseInt(sage.getText()));
        if(smale.isSelected())
        {
            us.setGender("male");
        }
        else
        {
            us.setGender("female");
        }
        JOptionPane.showMessageDialog(this, "Staff Member Updated Successfully!");
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Staff Member Not Found!", "Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

