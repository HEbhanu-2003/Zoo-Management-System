    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        String memberID = sid.getText();
        Staff sp = zc.searchStaff(memberID);
        if(sp != null)
        {
            JOptionPane.showMessageDialog(this, "Member Found!");
            sname.setText(sp.getName());
            sjr.setText(sp.getRole());
            sage.setText(String.valueOf(sp.getAge()));
            sconnum.setText(sp.getContactNumber());
            if("male".equals(sp.getGender()))
            {
                smale.setSelected(true);
            }
            else
            {
                sfemale.setSelected(true);
            }
            
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Member Not Found!", "Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

