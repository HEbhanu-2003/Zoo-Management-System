    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String animalID = aid.getText();
        String animalName = aname.getText();
        String species = asname.getText();
        String scientificName = asname.getText();
        String ageText = aage.getText();
        
        if(animalID.isEmpty() || animalName.isEmpty() || species.isEmpty() || scientificName.isEmpty() || ageText.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Please Fill In All Fields.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int age;
        try {
          age = Integer.parseInt(ageText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Age Must Be A Valid Number.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
    }
        String gender;
        
        if(amale.isSelected())
        {
            gender = "male";
        }
        else
        {
            gender = "female";
        }
    zc.addAnimal(animalID, animalName, species, scientificName, age, gender);
        
    // Success - you can now store or process the data
    JOptionPane.showMessageDialog(this, "Animal Details Submitted Successfully!");

        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String animalID = aid.getText();
        Animal ua = zc.searchAnimal(animalID);
        if(ua != null)
        {
            ua.setID(aid.getText());
        ua.setName(aname.getText());
        ua.setSpecies(aspecies.getText());
        ua.setScientificName(asname.getText());
        ua.setAge(Integer.parseInt(aage.getText()));
        if(amale.isSelected())
        {
            ua.setGender("male");
        }
        else
        {
            ua.setGender("female");
        }
        JOptionPane.showMessageDialog(this, "Animal Updated Successfully!");
        }
        
        else
        {
            JOptionPane.showMessageDialog(this, "Animal Not Found!", "Message", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

