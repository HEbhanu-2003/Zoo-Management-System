    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        String animalID = aid.getText();
        Animal sa = zc.searchAnimal(animalID);
        if(sa != null)
        {
            JOptionPane.showMessageDialog(this, "Animal Found!");
            aname.setText(sa.getName());
            aspecies.setText(sa.getSpecies());
            asname.setText(sa.getScientificName());
            aage.setText(String.valueOf(sa.getAge()));
            if("male".equals(sa.getGender()))
            {
                amale.setSelected(true);
            }
            else
            {
                afemale.setSelected(true);
            }
            
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Animal Not Found!", "Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

